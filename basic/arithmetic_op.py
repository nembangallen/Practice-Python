a = 2 # int
b = 6.0 # float
c = 12 + 0j # complex number
# Addition
print(a+b) # int + float, results to float
# Subtraction
print(b-a) # float - int,  results to float
print(a*7) # int * int, no widening is required
print(c/b) # complex / float
print(16/5) # gives int
print(20/5) # gives int
print(16//5) # gives quotient
print(16%5) # gives remainder

